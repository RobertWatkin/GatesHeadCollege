package weekone.friday.ifElse;

public class IfElseStatements {
    public static void main(String[] args) {
        /*
        if (condition){
            Do Something
        }
        else if (condition two){
            Do something else
        }
        else{
            Default functionality
        }

        Example Below
         */

        if(5 == 3){
            System.out.println("5 is equal to 3");
        }
        else if(5 == 4){
            System.out.println("5 is equal to 4");
        }
        else{
            System.out.println("5 is not equal to 3 or 4");
        }

    }
}
